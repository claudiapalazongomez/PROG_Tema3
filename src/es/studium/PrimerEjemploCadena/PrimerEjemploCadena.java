package es.studium.PrimerEjemploCadena;

public class PrimerEjemploCadena
{

	public static void main(String[] args)
	{
		String cadena;
		cadena = "Hola mundo";
		System.out.println(cadena);
		System.out.println(cadena.length());
		System.out.println(cadena.toUpperCase()); //.toLowerCase para ponerlo en minúscula
		System.out.println(cadena.charAt(1)); //te muestra el carácter que está en esa posición

	}

}
